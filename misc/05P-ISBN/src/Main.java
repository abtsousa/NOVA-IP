import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        String code = in.next(); in.nextLine();
        Tester iban = new Tester(code);
        if (iban.isValid()) {
            System.out.println("OK");
        } else {
            int VD = iban.calculateVD();
            String VDString = Integer.toString(VD);
            System.out.println("ERRO "+VDString);
        }
        in.close();
    }
}

