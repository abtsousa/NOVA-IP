import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        //input start
        Scanner in = new Scanner(System.in);
        int bilhetes = in.nextInt(); in.nextLine();
        int amigos = in.nextInt(); in.nextLine();
        int[] pedidos = new int[amigos];
        for (int i=0; i<amigos; i++) {
            pedidos[i] = in.nextInt();in.nextLine();
        }
        Ticketline distrib = new Ticketline(bilhetes, amigos, pedidos);

        in.close();

        //output start
        int[] enviados = distrib.distributeTickets();
        for (int i=0; i<amigos; i++) {
            System.out.println(enviados[i]);
        }


    }
}


